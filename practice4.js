//Question -- Use switch statement to print the day of week using the variable day with values 1 to 7.

let day = 6;
switch (day) {
        case 1:
                console.log("Monaday");
                break;
        case 2:
                console.log("Tuesday");
                break;

        case 3:
                console.log("Wednesday");
                break;

        case 4:
                console.log("Thrusday");
                break;

        case 5:
                console.log("Friday");
                break;

        case 6:
                console.log("Saturday");
                break;
        case 7:
                console.log("Sunday");
                break;
}